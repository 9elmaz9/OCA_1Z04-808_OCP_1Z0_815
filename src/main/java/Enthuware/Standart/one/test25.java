/**
 * Вопрос:
 * Какие из следующих являются допустимыми операторами в Java?
 *
 * Разбор:
 * ! (логическое НЕ):
 *
 * Да, это допустимый оператор.
 * Используется для инверсии логического значения. Например:
 * java
 * Copy code
 * boolean a = true;
 * System.out.println(!a); // Выведет: false
 * ~ (побитовое НЕ):
 *
 * Да, это допустимый оператор.
 * Используется для инверсии всех битов числа. Например:
 * java
 * Copy code
 * int a = 5; // 0000 0101
 * System.out.println(~a); // Выведет: -6 (инверсия битов)
 * & (побитовое AND или логическое AND):
 *
 * Да, это допустимый оператор.
 * Используется для выполнения побитового AND или логического AND (в зависимости от типа операндов). Например:
 * java
 * Copy code
 * int a = 5; // 0000 0101
 * int b = 3; // 0000 0011
 * System.out.println(a & b); // Выведет: 1 (0000 0001)
 * %= (остаток с присваиванием):
 *
 * Да, это допустимый оператор.
 * Используется для вычисления остатка от деления с присваиванием. Например:
 * java
 * Copy code
 * int a = 10;
 * a %= 3; // a = a % 3
 * System.out.println(a); // Выведет: 1
 * $:
 *
 * Нет, это не оператор.
 * Символ $ допустим в именах идентификаторов, но сам по себе не является оператором.
 * Правильные ответы:
 * !
 * ~
 * &
 * %=*/